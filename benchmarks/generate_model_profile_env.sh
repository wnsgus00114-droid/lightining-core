#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT_DIR"

mkdir -p build/benchmarks

CJ_TR_SWEEP=1 CJ_TR_WARMUP=${CJ_TR_WARMUP:-2} CJ_TR_ITERS=${CJ_TR_ITERS:-8} CJ_TR_BATCH=${CJ_TR_BATCH:-2} ./build/benchmarks/bench_transformer >/dev/null
CJ_LSTM_SWEEP=1 CJ_LSTM_WARMUP=${CJ_LSTM_WARMUP:-2} CJ_LSTM_ITERS=${CJ_LSTM_ITERS:-8} CJ_LSTM_STEPS=${CJ_LSTM_STEPS:-16} ./build/benchmarks/bench_lstm_rnn >/dev/null
CJ_CNN_SWEEP=1 CJ_CNN_WARMUP=${CJ_CNN_WARMUP:-2} CJ_CNN_ITERS=${CJ_CNN_ITERS:-8} ./build/benchmarks/bench_cnn_dnn >/dev/null
CJ_VLM_SWEEP=1 CJ_VLM_WARMUP=${CJ_VLM_WARMUP:-2} CJ_VLM_ITERS=${CJ_VLM_ITERS:-8} CJ_VLM_BATCH=${CJ_VLM_BATCH:-2} ./build/benchmarks/bench_vlm >/dev/null

OUT="build/benchmarks/model_runtime_profile.env"
{
  echo "# generated by benchmarks/generate_model_profile_env.sh"
  [[ -f build/benchmarks/transformer_runtime_profile.env ]] && cat build/benchmarks/transformer_runtime_profile.env
  [[ -f build/benchmarks/lstm_rnn_runtime_profile.env ]] && cat build/benchmarks/lstm_rnn_runtime_profile.env
  [[ -f build/benchmarks/cnn_dnn_runtime_profile.env ]] && cat build/benchmarks/cnn_dnn_runtime_profile.env
  [[ -f build/benchmarks/vlm_runtime_profile.env ]] && cat build/benchmarks/vlm_runtime_profile.env
} > "$OUT"

echo "[profile] wrote $OUT"
